package com.webdeninteractive.xbotts.Mapping.maptool;

public class TransformerTypeDescriptor {
	private String group;
	private String transformerTypeClass;
	private String name;

	public String getGroup() {
		return group;
	}

	public void setGroup(String group) {
		this.group = group;
	}

	public String getTransformerTypeClass() {
		return transformerTypeClass;
	}

	public void setTransformerTypeClass(String transformerTypeClass) {
		this.transformerTypeClass = transformerTypeClass;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
